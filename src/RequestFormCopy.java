    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        Helper helper = new Helper();
        
        String name = nameField.getText();
        String idNumber = idNumberField.getText();
        String cellNumber = cellNumberField.getText();
        String course = (String) courseComboBox.getSelectedItem();
        String yearLevel = (String) courseComboBox.getSelectedItem();
        String section = sectionField.getText();
        
        // table
        
        String purpose = purposeField.getText();
        String approvedBy = approvedByField.getText();
        Date borrowDate = borrowedDateCalendar.getDate();
        Date dueDate = dueDateCalendar.getDate();
        
        // checking
        if(name.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the name field.");
            return;
        }
        if(idNumber.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the id number field.");
            return;
        }
        if(cellNumber.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the cell number field.");
            return;
        }
        if(section.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the section field.");
            return;
        }
        
        if(!helper.checkRequestValues(requestTable)) {
            return;
        }
        if(purpose.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the purpose field.");
            return;
        }
        if(approvedBy.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in the approved by field.");
            return;
        }
        // date
        if (borrowDate != null && dueDate != null) {
            if (borrowDate.after(dueDate)) {
                JOptionPane.showMessageDialog(this, "Borrowed date should before the due date.");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select both borrow and due dates.");
            return;
        }
        
        // SAVE
        // name, idNumber, cellNumber, course, yearLevel, section, purpose, approvedBy, borrowDate, dueDate
        if(helper.saveRequestForm(name, idNumber, cellNumber, course, yearLevel, section, purpose, approvedBy, borrowDate, dueDate, requestTable)) {
            JOptionPane.showMessageDialog(rootPane, "Request submitted successfully.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Failed to submit request. Please try again.");
        }
        
        // RESET
        UserData.getInstance().resetCheckBoxItems();
        this.dispose();
    }//GEN-LAST:event_saveBtnActionPerformed

